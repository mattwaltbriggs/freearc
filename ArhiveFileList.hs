type SimpleTable k v = IORef [(k, v)]

newSimpleTable :: IO (SimpleTable k v)
newSimpleTable = newIORef []

lookupSimpleTable :: (k -> k -> Bool) -> SimpleTable k v -> k -> IO (Maybe v)
lookupSimpleTable eq ref key = do
  tbl <- readIORef ref
  return $ fmap snd $ find (¥(k,_) -> eq k key) tbl

insertSimpleTable :: (k -> k -> Bool) -> SimpleTable k v -> k -> v -> IO ()
insertSimpleTable eq ref key value = modifyIORef ref (¥tbl -> (key, value) : filter (¥(k,_) -> not (eq k key)) tbl)

