  let scan_bytes = min aSCAN_MAX arcsize  -- 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 4096 魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ, 魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ :)

  withPool $ ¥pool -> do
